സൗരയൂഥം അന്തർഗ്രഹങ്ങൾ&#44; ഉൽക്കാവലയം&#44; ബാഹ്യ ഗ്രഹങ്ങൾ എന്നിങ്ങനെ വിഭജിച്ചിരിക്കുന്നു.
മറ്റുചിലർ മാധ്യമങ്ങളിൽ ജോലി ചെയ്തുകൊണ്ടുതന്നെ ബ്ലോഗിങ്ങ് ചെയ്യുന്നവരാണ്.
ഡെക്കാൻ മേഖലയിലാണ്‌ ജോവർ കൂടുതലായും കൃഷി ചെയ്യുന്നത്.
