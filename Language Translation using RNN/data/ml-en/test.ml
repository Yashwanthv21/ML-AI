അത് മുലപ്പാൽ ഗ്രന്ഥികളുടെ എണ്ണത്തേയും വലിപ്പത്തേയും മാത്രം ആശ്രയിച്ചിരിക്കുമ്പോൾ വലിപ്പം പേശികളേയും കൊഴുപ്പിന്റെ അംശത്തേയും മറ്റും ആശ്രയിച്ചിരിക്കുന്നു.
എന്നപോലെ പ്രവർത്തിക്കുന്നു.റ്റിസിപി/ഐപി യിലെ ഹോസ്റ്റ് എന്നത് ഒരു കം‌പ്യൂട്ടർ ആണ്.ഒരു ഇന്റർനെറ്റ് അനേകം സ്വതന്ത്രങ്ങളായ ഭൗതികനെറ്റ്വർകുകളുടെ(ഉദാഹരണത്തിന് ലാൻ) ആന്തരികമായ കണക്ഷൻ ആണ്.ഇത്തരത്തിലുള്ള എല്ലാ കണക്ഷനുകളേയും ഒരു വലിയ നെറ്റ്വർക് ആയി റ്റിസിപി/ഐപി പരിഗണിക്കുന്നു.വ്യതിരിക്തങ്ങളായ ഭൗതികനെറ്റ്വർകുകളിലേക്കല്ല മറിച്ച്&#44;ഇത്തരം വലിയ നെറ്റ്വർകുകളിലേക്കാണ് ഹോസ്റ്റുകളെ ബന്ധിപ്പിക്കുന്നത്.
നദികളും അവയുടെ തോടുകളും തലങ്ങും വിലങ്ങുമുള്ള കേരളത്തിൽ വള്ളങ്ങളായിരുന്നു ഇരുപതാം നൂറ്റാണ്ടിന്റെ പകുതി വരെ മുഖ്യയാത്രാവാഹനങ്ങൾ.
