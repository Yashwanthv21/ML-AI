ബ്ലോഗുകളെക്കുറിച്ച്‌ സമീപവർഷങ്ങളിലുണ്ടായ അമിതാവേശം കെട്ടടങ്ങിത്തുടങ്ങിയതിന്റെ സൂചനയാണ്‌&#44; ഉപേക്ഷിക്കപ്പെടുന്ന ബ്ലോഗുകളുടെ എണ്ണപ്പെരുക്കമെന്ന്‌ ചൂണ്ടിക്കാണിക്കപ്പെടുന്നു.
മലയാളചലച്ചിത്രരംഗത്ത് മൂന്നു പതിറ്റാണ്ടുകളായി സജീവമായി അഭിനയിച്ചുകൊണ്ടിരിക്കുന്ന നടനാണ് മോഹൻലാൽ.
