എന്നാൽ ചന്ദ്രഗുപ്തൻ ശക്തമായ പ്രതിരോധം ഒരുക്കിയതിനാൽ രണ്ടു വർഷക്കാലം കാര്യമായ ലാഭമൊന്നും സെലൂക്കസിന് ഉണ്ടായില്ല എന്നു മാത്രമല്ല&#44; അവസാനം സന്ധിയിൽ ഏർപ്പെടാൻ നിർബന്ധിതനായിത്തീരുകയും മകളെ ചന്ദ്രഗുപ്തന് വിവാഹം ചെയ്ത് കൊടുക്കുകയും ചെയ്തു.
2001 മാർച്ച് 16നു ആരംഭിച്ച ജർമ്മൻ ഭാഷയിലുള്ള വിക്കിപീഡിയയാണ് രണ്ടാമത്തെ വിക്കിപീഡിയ.
മാനവൻ എന്നും മലയാളത്തിൽ പര്യായമുണ്ട്.
right|250px|thumb| മേയ്ഫ്ലവർ എന്ന കപ്പൽ പ്ലിമത്ത് തീരത്ത്.
കൃഷി ചെയ്യാൻ തുടങ്ങിയതോടെ അവർ വീടിനെക്കുറിച്ചും ചിന്തിക്കാൻ തുടങ്ങിയിരിക്കണം.
മുഹമ്മദ് നബിയുടെ നിർ‌‍ദ്ദേശങ്ങൾ&#44; കൽപന&#44; അനുവാദം&#44; മാതൃക എന്നിവയെ പൊതുവിൽ നബിചര്യ അഥവാ സുന്നത്ത് എന്നറിയപ്പെടുന്നു.
പ്രധാനമായും രണ്ടുഭാഗമായാണ്‌ രാമായണം വികസിച്ചിരിക്കുന്നത്‌ എന്നാണ്‌ പാശ്ചാത്യചരിത്രകാരന്മാരുടെ അഭിപ്രായം.
ഇന്ന് ഇൻറർ‍നെറ്റ് ടെലഫൊണി ഉപയോഗിച്ച് പ്രവർത്തിക്കുന്ന ധാരാളം ഉപകരണങ്ങൾ വിപണിയിൽ ലഭ്യമാണ്.
മോഹിനിയാട്ടം&#44; കൂടിയാട്ടം&#44; തുള്ളൽ&#44; ചാക്യാർ കൂത്ത് എന്നിവയും പാഠ്യവിഷയങ്ങളാണ്.സംഗീതം&#44; നാടകം എന്നീ കലകളുടെ അഭിവൃദ്ധിക്കായി കേരള സർക്കാർ 1958-ൽ തുടങ്ങിയ സ്ഥാപനമാണ്‌ സംഗീത നാടക അക്കാദമി.
അഭിപ്രായങ്ങൾ
വിദേശാധിപത്യത്തിനെതിരെ നടന്ന നിരവധി സമരങ്ങളാണ് പിന്നീടുള്ള കേരളചരിത്രത്തിലെ പ്രധാന ഏടുകളെല്ലാം.
ഇരുപതാം നൂറ്റാണ്ടിൽ ജ്യോതിശാസ്ത്രം&#44; നിരീക്ഷണ ജ്യോതിശാസ്ത്രം (Observational Astronomy) സൈദ്ധാന്തിക ജ്യോതിർഭൗതികം (Theoretical Astrophysics) എന്നിങ്ങനെ രണ്ടായി വിഭജിക്കപ്പെട്ടു.
ഇപ്രകാരം ഉള്ള പല പാതകളും മനുഷ്യർ പിന്നീട് റോഡുകളാക്കി മാറ്റിയിട്ടുണ്ട്.
പാകിസ്താനിൽ
ഈ സ്നേഹപ്രകടനം സാധാരണയായി ചുംബനം നൽകിയും&#44; തുമ്പിക്കൈ കോർത്തും&#44; തുമ്പിക്കൈ മറ്റേയാനയുടെ വായിൽ വച്ചും ഒക്കെയാണ് ചെയ്യാറുള്ളത്.
ഏതായാലും ലിന്നേയൻ സഭയിലെ അവതരണം വലിയ വാർത്തയൊന്നും ആയില്ല.
ഭൗതികശാസ്ത്രം&#44; സാമൂഹികശാസ്ത്രം&#44; പ്രകൃതിശാസ്ത്രം&#44; സാങ്കേതികശാസ്ത്രം പ്രസിദ്ധീകരണവിഭാഗം എന്നിങ്ങനെ അഞ്ച് അക്കാദമിക വിഭാഗങ്ങൾ ഉണ്ട്.
ഇസ്‌ലാമിക വിശ്വാസപ്രകാരം പ്രവാചകന്മാർ മനുഷ്യരിൽനിന്നും തിരഞ്ഞെടുക്കപ്പെട്ടവരാണ്.
കേരളത്തിലെ പത്രസ്ഥാപനങ്ങളുടെ കേന്ദ്രമായ കോട്ടയത്തിനപ്പുറത്തേക്ക്‌ മനോരമയുടെ പ്രചാരം വളർന്നു.
അതായത്‌ ഒരു ലേഖനത്തിനു പിന്നിൽ പ്രവർത്തിച്ചവർ പലരുണ്ടാവും.
നാഷണൽ സെന്റർ ഫോർ സൂപ്പർ കമ്പ്യൂട്ടിംഗ് ആപ്ലീക്കേഷനിൽ പ്രവർത്തിക്കുകയായിരുന്നു അക്കാലത്തു മാർക്ക് ആൻഡേഴ്സൺ.
തുടർന്ന് ചെക്കൊസ്ലൊവക്യയിലെ ജർമൻ ഭൂരിപക്ഷപ്രദേശമായ സുറ്റെൻലാൻഡ് എന്ന പ്രവിശ്യയിൽ ജർമനി അവകാശം ഉന്നയിച്ചു.‍ വെഴ്സൈൽസ് ഉടമ്പടി പ്രകാരം ചെക്കൊസ്ലൊവക്യയുടെ നിയന്ത്രണം ഫ്രാൻസ്&#44; ബ്രിട്ടൺ&#44; ഇറ്റലി എന്നീ രാജ്യങ്ങളുടെ കൈവശമായിരുന്നു.
622-ല് നടന്ന ഈ പലായനത്തെ ഹിജ്റ എന്നാണ് പറയുന്നത്.
ആദ്യ ദിവ്യബോധനം
ഉയർത്തപ്പെട്ട കടൽത്തീരങ്ങളാണ് അവയെന്ന് ഡാർവിൻ കരുതി.
ഒരു വ്യാഴാഴ്ചയാണ് ഈ സാധാരണ വർഷം ആരംഭിച്ചത്.
വേൾഡ് വൈഡ് വെബ്ബിന്റെ ഉപജ്ഞാതാ‍വായ ടിം ബർണേയ്സ് ലീ തന്നെയാണ് ആദ്യത്തെ ബ്രൌസറും സൃഷ്ടിച്ചത്.
ഉദാഹരണത്തിന് മുൻ പ്രോഗ്രാമിങ് ഭാഷകൾ ആസ്കി അക്ഷരങ്ങളെയായിരുന്നു ഉപയോഗിച്ചിരുന്നത്.
അദ്നാന്റെ പൂ‌ർ‌വ്വ ചരിത്രത്തെ പറ്റി വിവരങ്ങൾ ലഭ്യമല്ല; എങ്കിലും എബ്രഹാമിന്റെ വംശത്തിൽ പെട്ടയാളാണ്‌ എന്ന് ഐത്യഹ്യമുണ്ട്.
സാധാരണ ഉപയോഗിക്കുന്ന പ്രോട്ടോക്കോളുകൾ
ഇംഗ്ലണ്ടിൽ കെന്റിലെ ഡൗൺ എന്ന സ്ഥലത്ത് 1882 ഓഗസ്റ്റ് 19-ന് ഡാർവിൻ മരിച്ചു.
അദ്ദേഹം ജനിക്കുന്നതിനു രണ്ടുമാസം മുമ്പേ പിതാവായ അബ‌്ദുല്ലയും ആറ് വയസുള്ളപ്പോൾ മാതാവായ ആമിനയും മരിച്ചു പോയി.
ലൂണാർ പ്രൊസ്പെക്റ്ററിലെ (Lunar Prospector) ഗാമാ റേ സ്പെക്‌ട്രോമീറ്ററിൽ (gamma-ray spectrometer) നിന്നുള്ള വിവരങ്ങൾ വഴി തയ്യാറാക്കിയ ജിയോകെമിക്കൽ മാപ്പുകളുടെ പ്രത്യക്ഷവൽക്കരണം വഴി സമീപ പക്ഷ അർദ്ധഗോളത്തിലെ താപോല്പാദന മൂലകങ്ങളുടെ സാന്നിദ്ധ്യമാണ്‌ ഇതിനു നൽകപ്പെട്ട ഒരു വിശദീകരണം.
വ്യാപാരാവശ്യങ്ങൾക്കായി സിറിയയിലേക്ക് പോയ മുഹമ്മദിന്റെ പിതാവ് അബ്ദുല്ല തിരിച്ച് വരുന്ന വഴിയിൽ മദീനക്കടുത്ത് വെച്ച് രോഗബാധിതനായി മരണമടഞ്ഞു.
മറ്റൊരു പ്രധാന ലക്ഷ്യമായിരുന്നു പോയൻറ് 4875 (4875 മീറ്റർ ഉയരമുള്ള മറ്റൊരു മല) .
നന്നായില്ലെന്ന കാരണത്താൽ “വിചിത്രവിജയം” പ്രസിദ്ധികരിച്ചില്ല.
വിവാഹത്തിനുശേഷവും തന്റെ വിദ്യാഭ്യാസം അദ്ദേഹം തുടർന്നു.
ഒരിക്കൽ അദ്ദേഹം ഒരു ഹിന്ദുവാണോ എന്ന ചോദ്യത്തിന് ഗാന്ധിയുടെ മറുപടി ഇങ്ങനെയായിരുന്നു:
മരണസമയത്ത് ഇദ്ദേഹം പ്രൊപ്പഫോൾ&#44; ലോറാസെപാം മുതലായ മയക്കുമരുന്നുകൾക്ക് അടിമയായിരുന്നു.
സത്യം ലക്‌ഷ്യവും അഹിംസ ആ അതിലേക്കുള്ള മാർഗവുമാണ്‌.
കേരളത്തിന്റെ സാമൂഹികവികസനത്തെ കേരളാ മോഡൽ എന്ന പേരിൽ പല രാജ്യാന്തര സാമൂഹികശാസ്ത്രജ്ഞരും പഠനവിഷയമാക്കിയിട്ടുണ്ട്‌.
ലഘു ഭഗോഷ്ടങ്ങൾ (ചെറു യോനീപുടങ്ങൾ)
തിരുവിതാംകൂർ രാജാക്കന്മാര് കഥകളിക്ക് നൽകിയിട്ടുള്ള സംഭാവനകൾ ഏറെയാണ്.
എന്നാൽ വായ്ക്കു ചുറ്റുമുള്ളതും ചെവിക്കകത്തുമുള്ളതുമായ തൊലി വളരെ കട്ടികുറഞ്ഞതായിരിക്കും.
ടോർക്കട്ടോ കാർഡിലി - ഇറ്റാലിയൻ അംബാസഡർ.
ചിരിക്കുന്ന മരപ്പാവ (ചെറുകഥകൾ) (1975)
മലമുഴക്കി വേഴാമ്പലിനു ഔദ്യോഗിക പക്ഷിയുടേയും ഇന്ത്യൻ ആനയ്ക്ക് ഔദ്യോഗിക മൃഗത്തിന്റേയും സ്ഥാനമുണ്ട്.
തന്റെ പഠനത്തിന് സഹായകമായ വിവരങ്ങൾ കിട്ടാവുന്നിടത്തുനിന്നൊക്കെ സംഭരിക്കാൻ ഡാർവിൻ ശ്രമിച്ചു.
മറ്റു രാജ്യങ്ങളിൽ
നാലുകൂട്ടം ഉപ്പിലിട്ടതാണ്‌ കണക്ക്‌- കടുമാങ്ങ&#44; നാരങ്ങ&#44; ഇഞ്ചിപ്പുളി&#44; ഇഞ്ചിതൈര്‌.
ദാക്ഷിണാത്യപാഠം: തെക്കൻ പതിപ്പ്; കൂടുതൽ പ്രചാരത്തിലുള്ള പതിപ്പാണിത്.
കേരളത്തിലെ 14 റവന്യൂ ജില്ലകൾ 62 താലൂക്കുകൾ&#44; 1453 റവന്യൂ വില്ലേജുകൾ&#44; 1007 ഗ്രാമപഞ്ചായത്ത് എന്നിങ്ങനെ വിഭജിച്ചിരിക്കുന്നു.
