നിർമ്മാതാവ് പകർപ്പവകാശം നിലനിറുത്തുന്നുണ്ടെങ്കിലും വാറണ്ടി ഉപേക്ഷിക്കുകയും പകർപ്പെടുക്കാനും മാറ്റം വരുത്താനും അനുമതിനൽകുകയും ചെയ്യും.
വിഷയം ചില പ്രത്യേക വിഷയങ്ങളെക്കുറിച്ചാണ് ചില ബ്ലോഗുകൾ സംസാരിക്കുന്നത്.
സങ്കീർണ്ണതയിൽ മാത്രം വ്യത്യാസമുള്ള ഒരേതരം അവയവങ്ങൾ പങ്കിടുന്നവയാണ് എല്ലാ ജന്തുക്കളുമെന്നും&#44; അത്  പൊതുപൈതൃകത്തിന്റെ സൂചനയാണെന്നും ഗ്രാന്റ് വിശ്വസിച്ചിരുന്നു.
ഇതിൽ  എച്ച്.ടി.എം.എൽ 5 സ്പെസിഫിക്കേഷനിൽ പറയുന്ന  &#44; എന്നീ ടാഗുകൾ ഉൾപ്പെടുത്താൻ പദ്ധതികളുണ്ട്.
