സംസ്കൃതത്തിൽ ‘കാമം’ എന്നാൽ മോഹം&#44; അഭിനിവേശം&#44; താൽപ്പര്യം എന്നൊക്കെ അർത്ഥമാക്കാം.
മുഹമ്മദിനെ ചെകുത്താനെന്നാണ്  മാർട്ടിൻ ലൂഥർ വിശേഷിപ്പിച്ചത്.
എന്നും ഇന്റർനെറ്റ് ടെലിഫോണി എന്നും വോയിപ്പ് (VoIP) എന്നും ഒക്കെ ഈ സാങ്കേതികവിദ്യ അറിയപ്പെടുന്നു.
ബ്ലോഗിൽ നിന്നുള്ള പ്രധാന ഭാഗങ്ങൾ അച്ചടിമാധ്യമങ്ങളിൽ എത്താനും ബ്ലോഗെഴുത്തുകാർ റേഡിയോയിലും ടെലിവിഷനിലും പ്രത്യക്ഷപ്പെടാനും തുടങ്ങി.
രണ്ടാം ശതകത്തിലേതാണെന്നാണ്‌ ഫാദർ.
സൈബർ ജേർണലിസ്റ്റ് ഡോട്ട് നെറ്റിന്റെ ജെ-ബ്ലോഗ് ലിസ്റ്റ് പ്രകാരമാണിത്.
ഇവയെ കൂടാതെ ചൈനയിൽ ഉണ്ടായിരുന്ന ഇളം പിങ്ക് നിറത്തിലുള്ള കൊമ്പുകളോടുകൂടിയ ചൈനീസ് ആന (Elephus maximus rubridens)&#44; സിറിയ&#44; ഇറാൻ&#44; അഫ്ഗാനിസ്ഥാൻ എന്നിവിടങ്ങളിലുണ്ടായിരുന്ന&#44; ഏഷ്യൻ ആനകളിലെ ഏറ്റവും വലിയ ഇനമായിരുന്ന&#44; സിറിയൻ ആന ((Elephus maximus asurus) എന്നീ ഇനങ്ങൾ മനുഷ്യരുടെ ഇടപെടലുകൾകൊണ്ട് അന്യംനിന്ന ഏഷ്യൻ ആനകളിലെ ഇനങ്ങളാണ്.
