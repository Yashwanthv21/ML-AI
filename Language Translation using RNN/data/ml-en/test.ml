സസ്തനികളിൽ ഇത് മൂത്രം പുറത്തേയ്ക്ക് കളയുന്നതിനുള്ള അവയവമായും ഉപയോഗിക്കപ്പെടുന്നു.
പ്രാചീനശിലായുഗത്തിന്റെ മുക്കാലും പൂർവ്വകാലഘട്ടമായിരുന്നു.
പണ്ടുമുതൽക്കേ വാഴക്കുലയായിരുന്നു പ്രധാന കാഴ്ച.
അരിമാവുകൊണ്ട് കോലം വരച്ച് അതിനു മുകളിൽ കളിമണ്ണുകൊണ്ടുണ്ടാക്കിയ രൂപങ്ങൾ (തൃക്കാക്കരയപ്പൻ) പ്രതിഷ്ഠിക്കുന്നു.
ജനിച്ചയുടനെ ആനക്കുട്ടിക്ക് മൂന്നടിയോളം(തൊണ്ണൂറ് സെന്റീമീറ്റർ) ഉയരം ഉണ്ടാകും.
ഈ പ്രക്രിയയ്ക്ക് നാല് പ്രോട്ടോണുകൾ ആവശ്യമുള്ളതിനാൽ&#44; ഒരോ സെക്കന്റിലും 3.7  എണ്ണം (6.2  കിലോഗ്രാം) പ്രോട്ടോണുകൾ (അഥവാ ഹൈഡ്രജൻ അണുകേന്ദ്രങ്ങൾ) ഹീലിയം അണുകേന്ദ്രങ്ങളായി പരിവർത്തനം ചെയ്യപ്പെടുന്നു (ഏതാണ്ട് 8.9  സ്വതന്ത്ര പ്രോട്ടോണുകൾ സൂര്യനിൽ ഉണ്ടെന്ന് കണക്കാക്കപ്പെടുന്നു).
