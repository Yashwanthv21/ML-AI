അതികഠിനമായ ശൈത്യം മൂലം ഇരു രാജ്യങ്ങളും കാശ്മീരിലെ തങ്ങളുടെ ഏറ്റവും മുന്നിലെ കാവൽതുറകൾ ശൈത്യകാലത്ത് ഉപേക്ഷിക്കുകയും പിന്നീട് വസന്തകാലത്തോടെ അവയിൽ തിരിച്ചെത്തുകയും പതിവായിരുന്നു.
ഈ തരംഗങ്ങൾ മുകൾഭാഗത്തേക്ക് സഞ്ചരിക്കുകയും കൊറോണയിൽ വ്യാപിച്ച് അവിടെയുള്ള വാതകങ്ങളിൽ ഊർജ്ജം താപത്തിന്റെ രൂപത്തിൽ നിക്ഷേപിക്കുന്നു.
കഥകളിയുടെ അനുസാരിവാദ്യങ്ങളായ ചെണ്ട&#44; മദ്ദളം&#44; ചേങ്ങില&#44; ഇലത്താളം ഇവ സമന്വയിപ്പിച്ച് കൊണ്ടുള്ള മേളപ്രയോഗമാണു കേളികൊട്ട്.
അദ്ദേഹം ഇംഗ്ലണ്ടിൽ പോയ വർഷം തന്നെ അദ്ദേഹത്തിന് ആദ്യത്തെ കുട്ടി പിറന്നു&#44; രാംദാസ് ഗാന്ധി.
വർഷാവർഷങ്ങളിൽ മാറികൊണ്ടിരിക്കുന്നതും ഒരോ സോളാർ മാക്സിമത്തിനോടുത്തും (ഏതാണ്ട് 11 വർഷങ്ങൾ കൂടുമ്പോൾ) ദിശമാറുന്നതുമായ ശക്തമായ കാന്തികക്ഷേത്രം ഇതിനുണ്ട്.
ഇന്ത്യയുടെ ഭൂരിഭാഗം പ്രദേശങ്ങളും ബ്രിട്ടീഷ്‌ സാമ്രാജ്യത്തിനു കീഴിലുമായി.
കേരളത്തിന്റെ വടക്കേ അറ്റത്തെ വീതി 11 കി.
ഭൂമിക്കു ചുറ്റും നിതാന്തം സഞ്ചരിച്ച് രാത്രിയും പകലും സൃഷ്ടിക്കുന്നു.
