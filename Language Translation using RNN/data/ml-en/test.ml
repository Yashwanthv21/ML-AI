മരിയ
ഇളം ചാരനിറമുള്ള ഈ ആനകൾക്ക് വെള്ള പാടുകൾ മറ്റ് ഏഷ്യൻ ആനകളേക്കാൽ വളരെക്കുറവ് മാത്രമാണ് ഉണ്ടാകുക; മിക്കവാറും ചെവിയിൽ മാത്രം.
അതിവിനാശകാരിയായ ആയുധങ്ങൾ നിർമ്മിക്കുന്നുണ്ടെന്ന് ആരോപിച്ച് ഇറാഖിനെതിരെയും അമേരിക്ക ആക്രമണം നടത്തി.
ജാത്യാചാരങ്ങളുടെ അർത്ഥശൂന്യത വെളിവാക്കാനാണ്  ചണ്ഡാലഭിക്ഷുകിയിലൂടെ അദ്ദേഹം ശ്രമിക്കുന്നത്.
സമ്പദ് വ്യവസ്ഥ
ചില ചരിത്രകാരർ നോഹയും പുരാണ പരാമർശിതനായ മനുവും ഒരാൾ തന്നെ ആയിരിക്കാം എന്നും അവരുടെ(ഒരാൾ) പിന്മുറക്കാർ ഇന്ത്യയിൽ വാസമുറപ്പിച്ചതാവാം എന്നും അവരാണ്‌ പുരാതന ഇന്ത്യൻ സംസ്കാരത്തിൽ പ്രത്യക്ഷപ്പെടുന്ന ദ്രാവിഡർ എന്നും അഭിപ്രായപ്പെടുന്നുണ്ട്.
ഏകതാനമായി ഭ്രമണം ചെയ്യുന്ന വികിരണ മേഖലയുടെ പാളിയും വിഭിന്ന രീതിയിൽ ഭ്രമണം ചെയ്യുന്ന സം‌വഹന മേഖലയുടെ പാളിയും ഒത്തുചേരുന്ന ഭാഗമാണിത്&#44; ഇവിടെ ഒരു പാളി മറ്റൊരു പാളിയുടെ മീതെ തെന്നി നീങ്ങുന്നു.
വിജ്ഞാനത്തിലേക്കു നയിക്കുന്ന ഡേറ്റയുടെ അർത്ഥവത്തായ ഘടകങ്ങളാണ്‌ ഇൻഫർമേഷൻ.
അറൂനൂറോളം നെല്ലിനങ്ങൾ കേരളത്തിൽ കൃഷിചെയ്യുന്നുണ്ട്.
