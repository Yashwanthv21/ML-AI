ധ്രുവപ്രദേശങ്ങളിലെ റിഗോലിത്തിൽ ഹൈഡ്രജന്റെ അളവ് വളരെയധികമാണെന്ന് ലൂണാർ പ്രോസ്പെക്റ്ററിന്റെ ന്യൂട്രോൺ സ്പെക്ട്രോമീറ്ററും കണ്ടെത്തി.
