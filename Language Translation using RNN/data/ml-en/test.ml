ഐശ്വര്യത്തിന്റെ പ്രതീകമായി ഇതിനെ കാണുന്നു.
സാംസ്കാരിക സ്ഥാപനങ്ങൾ
ഛിന്നഗ്രഹങ്ങൾ&#44; ധൂമകേതുക്കൾ&#44; ഉൽക്കകൾ എന്നിവയുടെ പതനം മൂലമുണ്ടായ ഗർത്തങ്ങളാൽ (Craters) നിറഞ്ഞതാണ്‌ ചന്ദ്രോപരിതലം.
കേരളത്തിലെ ആദ്യത്തെ മരണം സംഭവിച്ചത് 2009 ഓഗസ്റ്റ് 11-ന്‌ തിരുവനന്തപുരത്താണ്‌.
മലയാള ഭാഷയുടെ ഉല്പത്തിയും പ്രാചീനതയും സംബന്ധിച്ച കാര്യങ്ങൾ ഇന്നും അവ്യക്തമാണ്.
ഇതിഹാസങ്ങളും പുരാണങ്ങളും രചിക്കപ്പെട്ട കാലത്തുതന്നെ കേരളത്തിന് ഒരു സ്വതന്ത്ര സ്വഭാവമുണ്ടായിരുന്നു.
അഭിനേതാവ് തന്നെ ഗാനം ചൊല്ലി ആടുന്ന രാമനാട്ടരീതിക്ക് മാറ്റം വരുത്തി പിന്നണിയിൽ ഗായകരുടെ പാട്ടിനനുസരിച്ച് നടൻ അഭിനയിക്കുന്ന രീതി കൊണ്ടുവന്നത്  വെട്ടത്തുനാടൻ സമ്പ്രദായമാണ്.
എന്നാൽ ഇക്കാര്യത്തിൽ നടന്ന പലപരീക്ഷണങ്ങളും ജാവ മറ്റുഭാഷകളോട് തുല്യമായതോ കൂടുതൽ മെച്ചപ്പെട്ടതോ ആയ വേഗത കാണിക്കുന്നുണ്ടെന്നു വെളിവാക്കി.
പരമേശ്വരൻ നായരുടെ അഭിപ്രായത്തിൽ മലയാളവും തമിഴും സ്വതന്ത്ര ഭാഷയായി രൂപപ്പെട്ടു വരുന്ന കാലത്തും കേരളത്തിന് ചോഴ&#44; പാണ്ടി ദേശക്കാരുമായി ബന്ധമുണ്ടായിരുന്നതിനാൽ ശക്തമായ സ്വാധീനം മലയാളത്തിൽ പ്രകടമായി ഉണ്ടായി.
