എന്നിട്ടും&#44; കുരങ്ങിൽ നിന്ന് മനുഷ്യൻ എന്ന ആശയത്തെ വിശ്വാസപ്രമാണമാക്കി മാറ്റുകയാണ് വംശോല്പത്തിയിൽ ഡാർവിൻ ചെയ്തതെന്നാണ് ആദ്യത്തെ നിരൂപണം തന്നെ അഭിപ്രായപ്പെട്ടത്.
2003-ലെ &quot;ലിവിങ് വിത് മൈക്ക്‌ൾ ജാക്സണ്‍&quot; എന്ന അഭിമുഖത്തിൽ&#44; തന്റെ കുട്ടിക്കാലത്തെ പീഡനങ്ങളേക്കുറിച്ച് പറഞ്ഞപ്പോൾ ഇദ്ദേഹം മുഖം മറച്ച് കരയുകയുണ്ടായി.
ജീവിതകാലത്ത് ആവിർഭവിക്കുന്ന സ്വഭാവങ്ങളിലൂടെയുള്ള ജൈവപരിണാമത്തെക്കുറിച്ചുള്ള ജീൻ ബാപ്റ്റിസ്റ്റ് ലാമാർക്കിന്റെ സിദ്ധാന്തത്തിന്റേയും ഡാർവിന്റെ മുത്തച്ഛനായ ഇറാസ്മസ് ഡാർവിന്റെ പരിണാമസംബന്ധിയായ ആശയങ്ങളുടേയും വക്താവായിരുന്നു ഗ്രാന്റ്.
സൂര്യനോട് ഏറ്റവും സമീപത്തുള്ള നക്ഷത്ര വ്യൂഹമായ ആൽഫാ സെന്റൗറിയിൽ നിന്നും വീക്ഷിക്കുകയാണെങ്കിൽ സൂര്യൻ കാശ്യപി നക്ഷത്രരാശിയിലായാണ്‌ കാണപ്പെടുക.
ഒരു അറബ് ഗോത്രവംശജൻറെ മുഖവും രാഷ്ട്ര നേതാവിൻറെ മുഖവും.
