വ്യവസ്ഥതെറ്റുമ്പോൾ തല്ലുകാരെ പിടിച്ചുമാറ്റുവാൻ റഫറി (ചായികാരൻമാർ അല്ലെങ്കിൽ ചാതിക്കാരൻമാർ) ഉണ്ട്‌.
ഭൂമിയെ പോലെയുള്ള ഗ്രഹങ്ങൾ മറ്റ് നക്ഷത്രങ്ങൾക്ക് ഉണ്ടോ?
ഷെരീഫ് പിന്നീട് 4&#44;000 പാകിസ്താനി പട്ടാളക്കാർ നഷ്ടപ്പെട്ടിട്ടുണ്ടെന്നു സമ്മതിച്ചു.
കറുത്ത നിറമുള്ള നീഗ്രോ വർഗ്ഗക്കാർ ആഫ്രിക്കയിലെ ഉഷ്ണ മേഖലയിൽ പെട്ട കാട്ടുപ്രദേശങ്ങളിലും അറേബ്യ&#44; മധ്യ-ദക്ഷിണ ഭാരതം&#44; ഓസ്ട്രേലിയ&#44; ടാസ്മേനിയ&#44; മലയ എന്നിവിടങ്ങളിലാണ്‌ കണ്ടു വരുന്നത്‌.
ഇങ്ങനെ ഗ്നു നിർമ്മിച്ച ടൂളുകളും ലിനക്സ് എന്ന കേർണ്ണലും ചേർത്ത് ഗ്നു/ലിനക്സ് എന്ന പേരിൽ ഓപ്പറേറ്റിംഗ് സിസ്റ്റം പുറത്തിറക്കി.
ആണ്ടിലൊരിക്കൽ പപ്പടവും ഉപ്പേരിയും കൂട്ടാനുള്ള അവസരമായിരുന്നു പണ്ടൊക്കെ സാധാരണക്കാരന്‌ ഓണം.
രാഷ്ട്രീയത്തടവുകാരെ മോചിപ്പിക്കുകയും യങ് ഇന്ത്യ പോലുള്ള പത്ര പ്രസിദ്ധീകരണങ്ങളുടെ മേലുള്ള നിരോധനങ്ങൾ പിൻ‍വലിക്കുകയും ചെയ്തില്ലെങ്കിൽ നിയമലംഘന പരിപാടികളിൽ നികുതിനിഷേധം കൂടി ഉൾപ്പെടുത്തുമെന്ന് അദ്ദേഹം വൈസ്രോയി റീഡിങ്ങ് പ്രഭുവിന് അന്ത്യശാസനം നൽകി.
വേദങ്ങളിൽ
രസതന്ത്ര ജ്യോതിശാസ്ത്രം (Astrochemistry):  ബഹിരാകാശത്ത് കാണുന്ന രാസ സംയുക്തങ്ങളെകുറിച്ച്&#44; പ്രത്യേകിച്ച് തന്മാത്ര മേഘ പടലങ്ങളിൽ&#44; അവയുടെ രൂപവത്കരണം&#44; ഇടപെടലുകൾ&#44; നശീകരണം ഇവയൊക്കെ പഠിക്കുന്ന ശാഖ.
ചൈന പാകിസ്താന്റെ സഖ്യകക്ഷിയാണെങ്കിലും പോരാളികളെ പിൻ‌വലിക്കാൻ ആവശ്യപ്പെട്ടു.
സൗർ ഗുഹയിൽ ഒളിച്ചിരുന്ന സമയത്ത് മഹമ്മദ് നബി അബൂബക്‌റിനെ ഇങ്ങനെ സമാധാനിപ്പിച്ചു
ഇവ പെട്ടെന്ന് ഭൂമുഖത്തു നിന്ന് അപ്രത്യക്ഷമാകുകയായിരുന്നു.
