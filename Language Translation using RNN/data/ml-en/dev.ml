മൊബൈൽ ഫോണുകളിൽ വിശ്വാസ്യതയോടെ ഉപയോഗിക്കാവുന്ന ഒരു പുതിയ വെബ്ബ് ബ്രൗസർ ഫെന്നാക് എന്ന പേരിൽ പുറത്തിറക്കുമെന്നും ബെക്കർ പ്രഖ്യാപിക്കുകയുണ്ടായി.
തെലുങ്കുസാഹിത്യത്തിലെ ആദ്യത്തെ രാമായണാഖ്യാനം തിക്കണ്ണ രചിച്ച നിർ‌വചനോത്തരരാമായണമാണ്‌.
