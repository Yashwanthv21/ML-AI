കേരളത്തിലെ ഹൈസ്ക്കൂൾ ഗണിതശാസ്ത്ര അദ്ധ്യാപകരുടെ മേൽനോട്ടത്തിൽ ആരംഭിച്ചിട്ടുള്ള  മാത്തമാറ്റിക്സ് ഇതിനൊരു ഉദാഹരണമാണ്.
വികാസം
മകൾ രോഗബാധിതയായപ്പോൾ&#44; സസ്യവിത്തിനങ്ങളേയും വളർത്തുമൃഗങ്ങളേയും ഉപയോഗിച്ചുള്ള പരീക്ഷണങ്ങൾ മാറ്റി വച്ച് ഡാർവിൻ അവൾക്കൊപ്പം കടലോരത്തുള്ള ഒരു സുഖവാസകേന്ദ്രത്തിൽ പോയി.
3.6 പതിപ്പ്
ഒരു വലിയ സന്നദ്ധസമൂഹം ഇതിനു പിന്നിലുണ്ടെങ്കിലേ ഇതൊക്കെ സാധ്യമാ‍വുകയുള്ളൂ.
ഇങ്ങനെയുള്ള സൗകര്യങ്ങൾ ഒരുക്കാൻ ട്രൈപ്പോഡ് പോലെയുള്ള വെബ് ഹോസ്റ്റിങ്ങ് കമ്പനികളും&#44; അമേരിക്ക ഓൺലൈൻ പോലെയുള്ള ഇന്റെർനെറ്റ് സേവനദാതാക്കളും&#44; സലോൺ പോലെയുള്ള ഇന്റെർനെറ്റ് പ്രസിദ്ധീകരണങ്ങളും&#44; യാഹൂ&#44; ഗൂഗിൾ തുടങ്ങിയ ഇന്റർനെറ്റ് പോർട്ടലുകളും പരസ്പരം മത്സരിക്കുന്നു.
ബിജെപിയായിരുന്നു പ്രധാന പ്രതിപക്ഷം.
തന്റെ മുപ്പത്തിയേഴാം വയസുമുതൽ മൂന്നര വർഷം അദ്ദേഹം വാർത്താപത്രങ്ങൾ വായിക്കാൻ വിസമ്മതിച്ചു.
എക്സ്പ്ലോറർ ബ്രൌസർ അവരുടെ ഓപ്പറേറ്റിംഗ് സോഫ്റ്റ്‌വെയറിനോടൊപ്പം നൽകി തുടങ്ങിയതോടു കൂടി മൈക്രൊസോഫ്റ്റ് പതിയെ പതിയെ നെറ്റ്സ്കേപ്പ് നാവിഗേറ്ററിന്റെ സ്ഥാനത്തേക്ക് കടന്നു കയറി.
ഈ കറുത്തപാടുകൾ സമീപപക്ഷത്തിൽ മാത്രമായി കാണപ്പെടുന്നു എന്നത് ശ്രദ്ധേയമാണ്&#44; ദൂരപക്ഷത്തിൽ അവ തീരെ ഇല്ല എന്നുതന്നെ പറയാം&#44; അവിടിവിടെയായി ഏതാനും പാടുകൾ മാത്രമേ (ഏതാണ്ട് ആ ഭാഗത്തിന്റെ 2% മാത്രം) ആ ഭാഗത്തുള്ളൂ.
ജാവ റൺറ്റൈം എൻ‌വിയറന്മെന്റ് അഥവാ ജെ.ആർ.ഇ.
ബലിക്കായി ഗോത്രവർഗ്ഗക്കാരും യജ്ഞങ്ങൾക്കായി ആര്യന്മാരും മൃഗങ്ങളെ ഹിംസിച്ചിരുന്നു.
പ്രദോഷമാഹാത്മ്യം
പക്ഷെ പിൽക്കാലത്ത് ഇതിന്‌ ശരിയായ രീതിയിൽ വിശദീകരണം നൽകിയത് ആൽബെർട്ട് ഐൻസ്റ്റൈനായിരുന്നു&#44; അദ്ദേഹത്തിന്റെ ദ്രവ്യ-ഊർജ്ജ സമവാക്യമായ   ഉപയോഗിച്ച് ഇതിന് വിശദീകരണം നൽകി.
ഇത്  വർണ്ണരാജിയിൽ 400 - 700 nm ഭാഗത്ത് വരുന്നു.
ഹൈന്ദവവിശ്വാസം അനുസരിച്ച് ത്രിമൂർത്തികളിൽ ഒരുവനും സംഹാരത്തിന്റെ ദേവനുമാണ് പരമശിവൻ.
1814-15 കാലഘട്ടത്തിൽ ഫ്രോൺഹോഫർ സൗര സ്‌പെൿട്രത്തിൽ 600-ഓളം രേഖകൾ കണ്ടെത്തി.
1837 ജൂൺ 20-ന് വിക്ടോറിയാ രാജ്ഞി അധികാരമേറ്റ ദിവസത്തിനടുത്ത്&#44; ഡാർവിൻ തന്റെ യാത്രക്കുറിപ്പുകളുടെ തിരുത്തിയെഴുതൽ പൂർത്തിയാക്കി.
സന്താനഗോപാലത്തിലെ അർജുനൻ&#44; യമപുരിയിൽ ചെന്നപ്പോൾ &quot;കള്ളുകുടിക്കും നായന്മാരുടെ പള്ളക്കിട്ടു കൊടുക്കണ കണ്ടു&quot; വത്രെ.
ഒന്നാം നൂറ്റാണ്ടിൽ ജീവിച്ചിരുന്ന അശ്വഘോഷനും കാളിദാസനും എല്ലാം രാമായണത്തെ അതിന്റെ ഇന്നത്തെ രൂപത്തിൽ തന്നെയാണ്‌ കണ്ടിരിക്കുന്നത്‌.
യൂറോപ്യൻ യൂണിയന്റെ സ്ഥാപക രാജ്യങ്ങളിലൊന്നാണ്‌.
അതുപോലെ തന്നെ പ്രണേതാവുമായുള്ള കൂടിക്കാഴ്ച സാധ്യമാക്കുന്നതിനുള്ള സന്ദേശങ്ങൾ കൈമാറ്റം ചെയ്യുന്നതിന് സ്ത്രീകൾ രഹസ്യഭാഷ ശൈലി പഠിക്കണമെന്നും കാമസൂത്ര അനുശാസിക്കുന്നുണ്ട്.
ഐ.ഇ വിൻഡോസുമായി ബന്ധപ്പെട്ടിരിക്കുന്നതിനാൽ ഐ.ഇയിൽ ഉണ്ടാകുന്ന ഇത്തരം സുരക്ഷാപാളിച്ചകൾ &#44;ഒരു ഇന്റർനെറ്റ് അക്രമകാരിക്ക് വിൻഡോസ് ഓപ്പറേറ്റിംഗ് സിസ്റ്റത്തിലേക്ക് നുഴഞ്ഞു കയറുന്നതിന്‌ സഹായകരമാകുന്നു.
ഈദുൽ ഫിത്ർ ചെറിയ പെരുന്നാൾ എന്നും ഈദുൽ അസ്‌ഹ ബക്രീദ്&#44; ബലി പെരുന്നാൾ എന്നീ പേരുകളിലും അറിയപ്പെടുന്നു.
മരുതം തിണയിൽ അതായത് തമിഴ് നാട്ടിൽ ആണ് കൂടുതൽ കൃഷി പണ്ടും എന്നതിന് ഇത് തെളിവാണ്.
കുമാരുവിനെ കൂടുതൽ പഠിപ്പിക്കണമെന്ന് അച്ഛന് ആഗ്രഹമുണ്ടായിരുന്നെങ്കിലും വലിയ തുക കൊടുത്ത് പഠിപ്പിക്കാൻ അന്നത്തെ സാമ്പത്തിക ചുറ്റുപാട് അനുവദിച്ചിരുന്നില്ല.
ഈ പ്രക്രിയ സ്പട്ടറിംഗ് എന്നറിയപ്പെടുന്നു.
2007 :    മികച്ച നടൻ  - പരദേശി
ഇവിടെ അർത്ഥം മനസ്സിലാകാത്തവർ ഈ സംഭാഷണത്തിൽ പരാമര്ശിക്കപ്പെട്ടത് യജമാനത്തി കാതിൽ അണിഞ്ഞിരുന്ന ആഭരണമായ ഓലയും ദാസി കയ്യിൽ കൊണ്ടുപോയിരുന്ന താളിയും ആണ് എന്നേ കരുതൂ.
ഉദാഹരണത്തിൽ ഫയർഫോക്സ് എന്ന പേരു് മാത്രം ലോഗോ ഇല്ലാതെ ഉപയോഗിക്കുവാൻ സാദ്ധ്യമല്ല.
കുറിപ്പുകൾ
അതേസമയം അദ്ദേഹം എല്ലാ മതങ്ങളും നല്ലതെന്ന് വിശ്വസിച്ചു.
ബൃഹത് ഭഗോഷ്ടങ്ങൾ (വൻ യോനീപുടങ്ങൾ)
1918-ൽ ആദ്യമായി കണ്ടുതുടങ്ങിയ ഈ അസുഖത്തിന്റെ കാരണകാരിയായ വൈറസുകൾ 2009 ആയപ്പോഴേക്കും നിരവധി ഉപവിഭാഗങ്ങളായി കണ്ടുതുടങ്ങി.
ലോബുകളെന്ന് അറിയപ്പെടുന്ന ഇവ ഡെയ്സിപൂക്കളുടെ ദളങ്ങൾ പോലെ ചിട്ടപ്പെടുത്തിയിരിക്കും.
2005 മികച്ച നടൻ - തന്മാത്ര
ലണ്ടണിൽ ഗവേഷണം തുടരുന്നതിനിടെ ഡാർവിന്റെ പരന്ന വായനയിൽ&#44; തോമസ് മാൽത്തൂസിന്റെ&#44; &quot;ജനസംഖ്യാ തത്ത്വത്തെക്കുറിച്ച് ഒരു പ്രബന്ധം&quot; (An Essay on the Principle of Population) എന്ന പുസ്തകവും ഉൾപ്പെട്ടു.
അദ്ദേഹത്തിന്റെ രചനകളുടെ ഈ പ്രത്യേകതകൾ കവിക്ക് വലിയ ജനപ്രീതി നേടിക്കൊടുത്തു.
പാരമ്പര്യത്തിന്റെ പ്രാധാന്യം വെളിവാക്കുന്ന ഗവേഷണത്തിന്റെ ഫലങ്ങൾ പരസ്യപ്പെടുത്തുകയും  ഗുണത്തികവുള്ളവരെ&#44; അവരുടെ &#39;ജാതി&#39;യിൽ നിന്നു തന്നെ വിവാഹം കഴിക്കാൻ പ്രേരിപ്പിക്കുകയും ചെയ്യണമെന്നായിരുന്നു ഗാൾട്ടന്റെ അഭിപ്രായം.
