ഗാന്ധിയും ടാഗോറും പരസ്പരം ആദരിക്കുകയും ബഹുമാനിക്കുകയും ചെയ്തിരുന്നവരായിരുന്നുവെങ്കിലും പലതവണ വളരെ നീണ്ട സം‌വാദങ്ങളിൽ ഏർപ്പെട്ടിട്ടുണ്ട്.
ഗാന്ധിജിയും ടാഗോറും പരിചയപ്പെടുന്നത് അങ്ങനെയാണ്.
എന്നിരുന്നാലും അദ്ദേഹം കൂടുതൽ സമയവും ആദ്ധ്യാത്മിക കാര്യങ്ങൾക്കാണ് ചിലവഴിച്ചത്.
പ്രൊസെല്ലാറം ക്രീപ് ടെറയ്നിലെ താപം പുറത്തുവിടുന്ന മൂലകങ്ങൾ മാന്റിലിനെ ചൂടാക്കുകയും ഭാഗികമായി ഉരുക്കുകയും ചെയ്തു എന്ന് കരുതപ്പെടുന്നു.
