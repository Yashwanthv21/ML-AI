നളചരിതം (നാല് ദിവസങ്ങൾ)
കാർട്ടൂണുകളും മുഖപ്രസംഗവുമൊഴികെ കാലാകാലങ്ങളിൽ വ്യത്യസ്തമായ ഉള്ളടക്കമാണ്‌ മനോരമ സ്വീകരിക്കുന്നത്‌.
ഡാർവിന്റെ സിദ്ധാന്തങ്ങളും രചനകളും ഗ്രിഗർ മെൻഡലിന്റെ പാരമ്പര്യസംബന്ധമായ സിദ്ധാന്തങ്ങളുമായി ചേർന്നുണ്ടായ &quot;നവ സംശ്ലേഷണം&quot;(Modern synthesis) ആധുനിക ജീവശാസ്ത്രത്തിന്റെയാകെ അടിസ്ഥാനമായിരിക്കുന്നു.
എന്നാൽ ആ പ്രശ്നം കോടതിക്കു പുറത്തുതന്നെ തീർന്നതിനാൽ നിയമപരമായ ശിക്ഷകളൊന്നും അദ്ദേഹത്തിന് ലഭിച്ചില്ല.
