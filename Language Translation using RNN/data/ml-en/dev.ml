ആപ്ലീക്കേഷനുകൾ ടാസ്ക്ക്ബാറിലേക്ക് കൂട്ടിച്ചേർക്കുകയുമാകാം.
ടെക്സാസ് എന്ന സംസ്ഥാനത്തിൻ ഇംഗ്ലണ്ടിന്റെ ഇരട്ടി വലിപ്പമുണ്ട്.
