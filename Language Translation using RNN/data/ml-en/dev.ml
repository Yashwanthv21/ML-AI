തമിഴിന്റെയും സംസ്കൃതത്തിന്റെയും സ്വാധീനത്തിൽ നിന്നു അകന്നു നിന്നു് നാടൻ ഈണത്തിൽ രചിക്കപ്പെട്ട കൃതിയെന്നുകൂടി കൃഷ്ണഗാഥയെ കുറിച്ച് പറയണം (അന്യഭാഷാസ്വാധീനം പൂർണ്ണമായും ഇല്ലെന്നല്ല; മണിപ്രവാളത്തിന്റെയും മധ്യയുഗങ്ങളിൽ തമിഴ്‌നാട്ടിൽ നിലനിന്നിരുന്ന ‘ഉന്തിപ്പാട്ടിൻറെയും’ സാദൃശ്യം കൃതിയിൽ ചൂണ്ടിക്കാണിക്കാവുന്നതുമാണു്) ഗൃഹാന്തരീക്ഷവും നാടോടിശീലുകളും തെളിമയാർന്ന മലയാള ഭാഷയും ചേർന്ന കൃഷ്ണഗാഥ മലയാളം കവിതയ്ക്ക് ഒരു പുതിയ പിറവി നൽകുകയാണുണ്ടായത്.
ഇതിനു മുകളിലാണ് സൂര്യന്റെ കൊറോണ സ്ഥിതി ചെയ്യുന്നത്&#44; കോറോണയിലെ താപനില 10-20 ലക്ഷം കെൽവിൻ വരെയായി ഉയരുന്നുണ്ട്.
ഖഗോള വസ്തുക്കളുടെ  ഭൗതികശാസ്ത്രം&#44; രസതന്ത്രം&#44; ചലനം&#44; അതോടൊപ്പം പ്രപഞ്ചത്തിന്റെ ഉൽ‌പത്തിയും വികാസവും ഈ പഠനങ്ങളുടെ ഭാഗമാണ്.
എന്നാൽ ഏറ്റവും കൂടുതൽ സംസ്ഥാനങ്ങളുമായി അതിർത്തി പങ്കിടുന്ന സംസ്ഥാനം ഉത്തർപ്രദേശാണ്‌.
അവലംബം
ഋഗ്വേദത്തിലോ മറ്റ് വേദങ്ങളിലോ രാമകഥയെപ്പറ്റി സൂചനകൾ ഇല്ല.
ജ്യോതിശാസ്ത്രജ്ഞന്മാർക്ക് ഇന്ന് ഖഗോളവസ്തുക്കളുടെ ചലനം കൂടുതൽ എളുപ്പത്തിൽ കൃത്യതയോടെ നിർണ്ണയിക്കാൻ പറ്റുന്നതിനാൽ ഇന്നു ഖഗോളവസ്തുക്കളുടെ ഭൗതിക യാഥാർത്ഥ്യങ്ങൾ (Physical nature) മനസ്സിലാക്കുന്നതിലാണ് അവർ കൂടുതൽ ശ്രദ്ധിക്കുന്നത്.
ഇത്തരം പല വ്യാപാര നിയമങ്ങളും കോളനികളിലെ കുടിയേറ്റ കർഷകർക്ക് ബുദ്ധിമുട്ടായിത്തോന്നിത്തുടങ്ങി.
മലബാർ എന്ന പേർ നൽകിയത് അറബികൾ ആണെന്നതും ഇതിന് ശക്തി പകരാൻ ചൂണ്ടിക്കാണിക്കപ്പെടുന്നു.
സ്തനങ്ങൾ സസ്തനികളുടെ നെഞ്ചോട് ചേർന്നാണ്‌ കാണുന്നത്&#44; മനുഷ്യനിൽ അത് വാരിയെല്ലുകളുടെ മുകൾ ഭാഗത്തായി അതായത് നെഞ്ചിലെ പെക്ടൊറാലിസ് എന്ന മാംസപേശിയോട് ചേർന്നാണ്‌ ഇവ കാണപ്പെടുന്നത്.
ഫയർഫോക്സിന്റെ സവിശേഷതകളിൽ ടാബ് ബ്രൗസിങ്ങ്&#44; സ്പെൽ ചെക്കർ&#44; ഇൻക്രിമെന്റൽ ഫൈൻഡ്&#44; ലൈവ് ബുക്ക് മാർക്കിങ്ങ്&#44; സഹജമായ ഒരു ഡൗൺലോഡ് മാനേജർ&#44;കീബോർഡ് ഷോട്ട്കട്ടുകൾ&#44; ഉപയോക്താക്കൾക്ക് തങ്ങൾക്ക് ഇഷ്ടമുള്ള സെർച്ച് എഞ്ചിൻ ഉപയോഗിച്ച് തിരയാൻ സാധിക്കുന്ന സഹജമായ ഒരു സെർച്ച് എഞ്ചിൻ എന്നിവയാണ്‌.
ഗ്രഹങ്ങൾക്കുള്ളതുപോലെ സൂര്യന്റെ ശരീരത്തിന്‌ വ്യക്തമായ അതിർത്തിയില്ല&#44; കേന്ദ്രത്തിൽ നിന്നും പുറത്തേക്ക് പോകുംതോറും സാന്ദ്രതയിൽ വലിയ കുറവു സംഭവിക്കുന്നു.
മുതിർന്ന കൊമ്പനാനകൾ കൊല്ലത്തിലൊരിക്കലായി മദപ്പാട് എന്ന ഒരു അവസ്ഥയിലെത്തുന്നു.
ചെറുതും&#44; പ്രായം കുറഞ്ഞതും&#44; ധൈര്യമില്ലാ‍ത്തതുമായ ആനകൾ ഇത്തരം അടയാളങ്ങൾ കാണുമ്പോൾ തന്നെ പിന്മാറും.
ഭൗമ-ചാന്ദ്രവ്യവസ്ഥയുടെ പരിണാമത്തിന്റെ ഫലമായി ചന്ദ്രൻ ഭൂമിയിൽ നിന്ന് അകന്നുപോകുന്നതോടെ ഇതിന്‌ മാറ്റം വരും.
മുഹമ്മദ്‌ എന്ന പേരാണ്‌ ഇമാം നിർദേശിച്ചിരുന്നതെങ്കിലും അല്ലാഹുവിന്റെ അനുയായിയായിരുന്ന മിഖായിലിന്റെ പേർ സ്വീകരിക്കാൻ തീരുമാനിച്ചെന്നും ഈ റിപ്പോർട്ടിൽ പറയുന്നു.
ബലരാമൻ
അതനുസരിച്ച് മാതാവായ ജാനകിയുടെ കൈവിരലിൽ ആറു വിരലുകൾ ഉണ്ടായിരുന്നു എന്നും അതിലൊന്ന് മുറിച്ച് ഭൂമിയിൽ നട്ടതിൽ നിന്നാണ്‌ ലോകത്തിലെ എല്ലാ ഇനങ്ങളിലുമുള്ള വിത്തുകൾ മുളച്ച് വന്നതെന്നും കാണുന്നു.
പക്ഷെ ഇക്കാലത്ത് പേർഷ്യയിലും മറ്റും ഇത് ഏറ്റവും ഔന്നത്യത്തിലായിരുന്നു.പ്രമുഖരായ അറബ് ജ്യോതിശാസ്ത്രജ്ഞരായ അൽ-ബത്വാനി&#44; താബിത് ഇബ്ൻ ഖുറ്ര തുടങ്ങിയവർ ജ്യോതിശാസ്ത്രത്തിന് വളരെ വലിയ സംഭാവനകൾ നൽകിയവരിൽ ചിലരാണ്.
മുഹമ്മദ് നബിയുടെ കുടുംബ ജീവിതം
പ്രസ്തുത വിശ്വാസം സ്വീകരിക്കാതെ ആരും മുസ്‌ലിംകളാവില്ലെന്നും ഇസ്‌ലാം ഉണർത്തുന്നു.
ഈ ആശയത്തെ അടിസ്ഥാനമാക്കി എൻക്വയർ (ENQUIRE) എന്നൊരു സം വിധാനം അദ്ദേഹം നിർമ്മിക്കുകയും ചെയ്തു.
ചരിത്രം
ജാവയുടെ ആദ്യപതിപ്പ് കേവലമൊരു ഫ്ലോപ്പി ഡിസ്കിൽ ഉൾക്കൊള്ളിക്കാമായിരുന്നു.
ഇവരെ പിന്നീട് ടൈഗർ മലയുടെ തെക്ക് കിഴക്കൻ ഭാഗങ്ങളും പരിയോം കാ താലാബ് എന്ന വടക്കൻ പ്രദേശവും പിടിക്കാനായി നിയോഗിച്ചു.
ഭൂമിയിൽ അനുഭവപ്പെടുന്ന ഭൂരിഭാഗം വേലിയേറ്റവും ചന്ദ്രന്റെ ഗുരുത്വാകർഷണം മൂലം അനുഭവപ്പെടുന്നതാണ്.
1996 ഓഗസ്റ്റിൽ പുറത്തിറക്കിയ ഇന്റർനെറ്റ് എക്സ്പ്ലോറർ 3.0-നോടൊപ്പം ജെസ്ക്രിപ്റ്റ് പിന്തുണ ഉൾക്കൊള്ളിക്കുകയും ചെയ്തു.
ശീലാവതി നാലുവൃത്തം
രണ്ടുവർഷത്തിനുള്ളിൽ അവരുടെ പ്രാമാണികത ഇല്ലാതാക്കുന്നതിൽ ഹക്സ്ലി വിജയിച്ചു.
എന്നാൽ കോടിക്കണക്കിന്‌ വർഷങ്ങൾക്കുശേഷമേ ഇത് സംഭവിക്കുകയുള്ളൂ.
മാക്സ് ലാബ് എന്റർടെയുൻമെന്റ്ഒരു സഹകരണ ചലച്ചിത്ര വിതരണ കമ്പനി.
കൈരളിയുടെ കഥ - എൻ.
ബ്ലോഗ്
കഴിഞ്ഞ ഒന്നര പതിറ്റാണ്ടിനിടെ മാത്രം മുപ്പതിലേറെ പുതിയ വൈറസുകൾ മനുഷ്യന്‌ ഭീഷണിയായി പ്രത്യക്ഷപ്പെട്ടിട്ടുണ്ട്‌.
20-30% മൃഗങ്ങളും വൃക്ഷലതാദികളും ഒന്നോ ഒന്നരയോ ഡിഗ്രി താപവർദ്ധനയുണ്ടായാൽ വംശനാശം നേരിടും
ഇതിനൊരു കാരണം സ്ഥിരം കൊളുത്തുകൾ വഴി നേരിട്ടു വരുന്നവർ ആ ബ്ലോഗിന്റെ ഉള്ളടക്കം നല്ലതാണെന്നും അതു കാണാൻ മൂല്യമുള്ളതുമാണെന്നു ലേഖകനു മനസ്സിലാക്കിക്കൊടുക്കുകയും ചെയ്യും എന്നതാവാം.
പ്രോഗ്രാം പ്രവർത്തിക്കാനാവശ്യമായ മെമ്മറി പ്രത്യേകം കുറിച്ചുകൊടുക്കണം എന്നുള്ളതുകൊണ്ടും&#44; അതു കൊണ്ടു തന്നെ ഹാർഡ്‌വെയറിനനുസൃതമായി പ്രോഗ്രാം പുതുക്കേണ്ടി വരുമെന്നതും പിൽക്കാലത്ത് സി++നു തിരിച്ചടിയായി.
കൃതികളുടെ പ്രത്യേകതകൾ
സുഹൃത്തുക്കളെ കണ്ടെത്താനും പഴയ സൗഹൃദം പുതുക്കാനുമൊക്കെ ഓർക്കട്ട് വഴി സാധ്യതയുണ്ട്.
നിയമലംഘന സമരം
തന്റെ 27 വർഷത്തിലധികം നീണ്ട അഭിനയജീ‍വിതത്തിൽ മാറ്റങ്ങളെ ഉൾക്കൊണ്ടു അഭിനയിച്ചിട്ടുള്ള ഒരു നടനാണ് ലാൽ.
അവതാരപ്രസ്താവങ്ങൾ കാണാവുന്ന മറ്റൊരു സന്ദർഭം അയോദ്ധ്യാകാൻഡത്തിന്റെ ആരംഭത്തിലാണ്‌.
ചന്ദ്രന്റെ ഗുരുത്വാകർഷണമണ്ഡലത്തിന്റെ പ്രധാന പ്രത്യേകത വലിയ ഗർത്തങ്ങളോടനുബന്ധിച്ച് ഗുരുത്വാകർഷണമണ്ഡലത്തിലുണ്ടാവുന്ന വർദ്ധനയാണ്‌.
മൈക്രോസോഫ്റ്റ് ജാവ വിൻഡോസിനൊപ്പം വിതരണം ചെയ്യുന്നതു നിർത്തി.
പിന്നീട് അധിനിവേശം ചെയ്തവരാണ്‌ ദ്രാവിഡർ.
ജാവ പ്ലഗ്-ഇൻ ഉള്ള ബ്രൌസറുകളിലാണ് ജാവ ആപ്‌ലറ്റുകൾ പ്രവർത്തിക്കുക.
