ഇത് അമേരിക്കക്കാർക്ക് കള്ളക്കടത്തലിലൂടെ ലഭിക്കുന്ന തേയിലയേക്കാൽ വില കുറച്ച് വിൽകാമെന്നായിരുന്ന് അവർ വിചാരിച്ചത്.
പശ്ചാത്തലം
ബ്രിട്ടീഷ് സർക്കാർ ഈ വാർഷികങ്ങളോടനുബന്ധിച്ച് ഇറക്കിയ രണ്ടു പൗണ്ടിന്റെ വിശിഷ്ട നാണയത്തിൽ ഡാർവിൻ ഒരു വലിയ ആൾക്കുരങ്ങിനു നേർക്കുനേർ നിൽക്കുന്നതായി ചിത്രീകരിച്ചിരിച്ചിട്ട് അതിനു താഴെ 1809 ഡാർവിൻ 2009 എന്നും&#44;  വിളുമ്പിൽ വംശോല്പത്തി - 1859 എന്നും രേഖപ്പെടുത്തിയിരിക്കുന്നു.
ഏറ്റവും സാധാരണയായി  നിരീക്ഷണത്തിനു ഉപയോഗിക്കുന്ന ഒരു ഉപകരണം electronic imagers-ഉം spectrographs-യും ഘടിപ്പിച്ച ദൃശ്യപ്രകാശ  ദൂരദർശിനി ആണ്.
മഹാജനപദങ്ങൾ എന്നാണ്‌ ഇവ അറിയപ്പെട്ടിരുന്നത്.
ടീമിന്റെ ചിഹ്നത്തിനെ ചുരുക്കി സ്റ്റോമ്പർ എന്ന് വിളിക്കപ്പെടുന്നു.
ബാബിലോണിയന്മാർ&#44; അസ്സീറിയന്മാർ&#44; ഹീബ്രുകൾ&#44; ഫിനീഷ്യന്മാർ&#44; അറബികൾ എന്നിവർ സെമറ്റിക്‌ വർഗ്ഗത്തിൽ പെട്ടവരും&#44; യുറോപ്പിന്റെ ഉത്തരാർദ്ധത്തിൽ താമസിച്ചിരുന്ന നോർഡിക്‌ വംശമ്മ് മധ്യ യൂറോപ്പിലെ ആൽപൈൻ വംശം ഇന്ത്യയിലും ജർമ്മനിയിലും മറ്റും വാസമുറപ്പിച്ച ആര്യന്മാർ എമ്മൊവർ ഇന്തോ യൂറോപ്യൻ വർഗ്ഗത്തിലും പെടുന്നു.
