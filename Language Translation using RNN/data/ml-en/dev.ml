വെബ് ബ്രൌസറിൽ നിന്നുകൊണ്ട് തന്നെ ആപ്ലിക്കേഷനുകൾ ക്ലയന്റ് കമ്പ്യൂട്ടറുകളിൽ ഇൻസ്റ്റോൾ ചെയ്യാനനുവദിക്കുന്നു.
പിന്നീട് കെപ്ലറുടേയും ന്യൂട്ടന്റേയും പ്രവർത്തനങ്ങൾ ഖഗോളവസ്തുക്കളുടെ ചലനത്തെ സംബന്ധിച്ച നിയമങ്ങൾ (celestial mechanics) വികസിപ്പിക്കുന്നതിലേക്ക് നയിച്ചു.
