വീലർ  അടക്കമുള്ള ചില സുരക്ഷാ വിദഗ്ദ്ധർ സാധാരണ ബ്രൗസിങ്ങ് ഉപയോഗിക്കുന്നതിനു പകരം മറ്റു ബ്രൗസറുകളിലേക്ക് ചേക്കേറണമെന്ന് അഭിപ്രായപ്പെടുന്നു.
സന്ദേശം എങ്ങനെ തുടങ്ങണം എങ്ങനെ അവസാനിക്കണം എന്നു നിശ്ചയിക്കുക.
പാശ്ചാത്യ സാഹിത്യത്തിൻറെ സ്വാധീനം മൂലം മലയാള സാഹിത്യലോകത്ത് വന്ന മാറ്റങ്ങളെ ആധുനിക സാഹിത്യമെന്നു വിവക്ഷിക്കുന്നു.
ഇൻറർ‍നെറ്റ് ടെലഫൊണിയ്ക്ക് ചില വ്യവസ്ഥകൾ അഥവാ പ്രോട്ടൊക്കോളുകൾ ഉണ്ട്.
ഒടുവിൽ&#44; ആ കുറിപ്പുകൾ&#44; ഔദ്യോഗിക വിവരണത്തിന്റെ പ്രകൃതിശാസ്ത്രസംബന്ധിയായ മൂന്നാം ഭാഗ‍മായി.
പാകിസ്താനിൽ നിന്നും ചിത്രത്തിനു നല്ലപ്രതികരണമാണുണ്ടായത്.
പിന്നീട് പുറത്തിറങ്ങിയ രാജാവിന്റെ മകന്‍ എന്ന ചിത്രം മോഹൻലാലിന് ധാരാളം ആരാധകരെ നേടിക്കൊടുത്തു.
