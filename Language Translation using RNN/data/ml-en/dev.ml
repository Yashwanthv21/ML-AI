Condensation പരികൽപന എന്നും അറിയപ്പെടുന്ന ഈ സിദ്ധാന്തമനുസരിച്ച് ഭൂമിയും ചന്ദ്രനും സൗരയൂഥത്തിന്റെ പ്രാരംഭദശയിൽ ഉണ്ടായിരുന്ന അക്ക്രീഷൻ ഡിസ്കിൽ നിന്ന് ഒരേ കാലയളവിൽ ഉണ്ടായതാണ്‌.
വെബ്ബറിനേയും യാക്കോബിയേയും പോലെ ദിനേശചന്ദ്രനും രാമകഥക്ക് രണ്ട് പ്രഭവസ്ഥാനങ്ങൾ ഉണ്ടെന്നു കരുതുന്നു.
