അതുകൊണ്ടാണ്‌ കവിയെ ആദികവിയെന്നും&#44; കാവ്യത്തെ ആദികാവ്യം എന്നും വിളിച്ച്‌ നിരൂപകർ ആദരിക്കുന്നതും.
